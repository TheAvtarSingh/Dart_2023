void main() {
  print("Sat Shri aakal Ji");
  // print("Hello");

  // Data Types
  var num = 10; // It is inferred as integer automatically by compiler
  print(num);
  // NOTE: All data types in Dart are Objects.
  // Therefore, their initial value is by default 'null'

  // Number
  // -- Int
  int a = 10;
  print(a);
  //  -- Double
  double b = 1.1;
  b = 1.41e5;
  print(b);

  //Strings
  String name = "Henry";
  print(name);

  // Boolean
  bool isValid = true;
  print(isValid);
}
